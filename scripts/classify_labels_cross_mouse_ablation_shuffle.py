def _resolve_device(name: str) -> torch.device:
    if name == "cpu":
        return torch.device("cpu")
    if name == "cuda":
        if not torch.cuda.is_available():
            raise RuntimeError("--device=cuda requested but CUDA is not available")
        return torch.device("cuda")
    if torch.cuda.is_available():
        return torch.device("cuda")
    return torch.device("cpu")


def _set_seed(seed: int) -> None:
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)


def _canonicalize_holdout_metrics(metrics: Dict[str, Any]) -> Dict[str, Any]:
    normalized = dict(metrics)
    if "accuracy" not in normalized and "mean_acc" in normalized:
        normalized["accuracy"] = normalized["mean_acc"]
    if "macro_f1" not in normalized and "mean_f1" in normalized:
        normalized["macro_f1"] = normalized["mean_f1"]
    return normalized


# ---------------------------------------------------------------------------
# Preflight validation — cross-mouse uses intersection of available IDs
# ---------------------------------------------------------------------------

def _preflight_check(
    eligible_mice: List[str],
    data_root: Path,
    cache_dir: Optional[Path],
    shuffle_type: str,
    method: str,
    p_active: int,
    per_trial_thresh: bool,
    clip_frames: int,
    different_shuffle_per_trial: bool,
    n_shuffles: int,
    max_trials: Optional[int],
    seed: int,
) -> List[int]:
    """Validate and return the sampled shuffle IDs (same set used for all mice).

    For cross-mouse coherence we take the intersection of available IDs across
    all mice, then randomly sample N from that intersection.

    Raises
    ------
    SystemExit
        With a clear per-mouse error message when any check fails.
    """
    fs = _FakeState(cache_dir=cache_dir, data_root=data_root)

    errors: List[str] = []
    per_mouse_available: Dict[str, List[int]] = {}

    for mouse_name in eligible_mice:
        mcdir = _resolve_mouse_cache_dir(fs, mouse_name)
        base_stem = build_vectorization_cache_stem(
            mouse_name=mouse_name,
            method=method,
            p_active=p_active,
            per_trial_thresh=per_trial_thresh,
            clip_frames=clip_frames,
        )
        available = _existing_shuffle_ids(
            mcdir,
            base_stem,
            shuffle_type,
            different_shuffle_per_trial,
        )

        # Check 1: Enough shuffles?
        if len(available) < n_shuffles:
            errors.append(
                f"  {mouse_name}: needs {n_shuffles} shuffles but only "
                f"{len(available)} available (ids={available}). "
                f"Missing: {n_shuffles - len(available)}"
            )
            continue

        # Check 2: Manifest max_trials consistency
        if max_trials is not None:
            manifest = _load_manifest(mcdir, shuffle_type)
            mkey = _manifest_key(
                shuffle_type,
                method,
                p_active,
                per_trial_thresh,
                clip_frames,
                different_shuffle_per_trial,
            )
            entry = manifest.get(mkey, {})
            stored_max = entry.get("max_trials_used", None)
            if stored_max is not None and stored_max < max_trials:
                errors.append(
                    f"  {mouse_name}: manifest records max_trials_used={stored_max} "
                    f"but --max-trials={max_trials} requested. "
                    f"Regenerate shuffles with at least max_trials={max_trials}."
                )
                continue

        per_mouse_available[mouse_name] = available

    if errors:
        print("\nPREFLIGHT ERRORS — cannot proceed:", flush=True)
        for e in errors:
            print(e, flush=True)
        sys.exit(1)

    # Compute intersection across all eligible mice
    id_sets = [set(ids) for ids in per_mouse_available.values()]
    common_ids = sorted(set.intersection(*id_sets) if id_sets else set())

    if len(common_ids) < n_shuffles:
        print(
            f"\nPREFLIGHT ERROR: only {len(common_ids)} shuffle IDs are present for ALL "
            f"mice (intersection={common_ids}), but {n_shuffles} requested.",
            flush=True,
        )
        print("Per-mouse availability:", flush=True)
        for mn, ids in per_mouse_available.items():
            print(f"  {mn}: {ids}", flush=True)
        sys.exit(1)

    rng = np.random.default_rng(seed)
    sampled = sorted(rng.choice(common_ids, size=n_shuffles, replace=False).tolist())
    return sampled


# ---------------------------------------------------------------------------
# Load mouse data from cache for a given shuffle ID
# ---------------------------------------------------------------------------

def _load_mouse_shuffle_data(
    *,
    mouse_name: str,
    shuffle_id: int,
    cache_path: Path,
    clip_frames: int,
) -> Optional[Dict[str, Any]]:
    """Load cached shuffled vectorization + shuffled grid paths for one mouse/shuffle.

    Returns a dict with the same field names as ``_load_mouse_data`` in the
    non-shuffle script so that the same 4-model training block can be reused.
    Raises ``FileNotFoundError`` if the shuffled grid cache is missing
    (run ``generate_trials_ablation_shuffle.py`` to regenerate).
    """
    cached = load_vectorization_cache(cache_path)
    x_vec = np.nan_to_num(np.asarray(
        cached["features"] if "features" in cached else cached["X"]
    ))
    labels = np.asarray(cached["labels"])
    trial_ids_cache = np.asarray(cached.get("trial_ids", np.arange(len(labels))))
    valid_frames_cache = np.asarray(cached.get("valid_frames", np.full(len(labels), clip_frames)))

    if len(np.unique(labels)) < 2:
        return None

    # Load shuffled grid paths saved by generate_trials_ablation_shuffle.py
    grid_paths = load_shuffled_grid_cache_paths(cache_path, trial_ids_cache.tolist())

    first_shape = np.load(grid_paths[0], mmap_mode="r").shape
    in_channels = int(first_shape[2])

    return {
        "mouse": mouse_name,
        "x_vec": x_vec,
        "labels": labels,
        "grid_paths": grid_paths,
        "grid_frames": valid_frames_cache,
        "in_channels": in_channels,
        "cache_path": str(cache_path),
        "vec_source": "shuffle_cache",
        "n_trials": int(len(labels)),
    }


