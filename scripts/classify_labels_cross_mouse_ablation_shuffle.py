# Cross-mouse LOMO for one shuffle ID
# ---------------------------------------------------------------------------

def _run_cross_mouse_one_shuffle(
    *,
    shuffle_id: int,
    eligible_mice: List[str],
    mouse_data: Dict[str, Optional[Dict[str, Any]]],
    clip_frames: int,
    model_order: List[str],
    device: torch.device,
    epochs_mlp: int,
    epochs_cnn1d: int,
    epochs_cnn3d: int,
    lr_vec: float,
    lr_cnn3d: float,
    batch_size_vec: int,
    batch_size_grid: int,
    weight_decay: float,
    early_stop_patience: int,
    num_workers_dl: int,
    seed: int,
) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    """Run LOMO for one shuffle_id across all eligible mice."""
    per_fold: Dict[str, Any] = {}
    cm_payload: Dict[str, Any] = {}

    available_mice = [m for m in eligible_mice if mouse_data.get(m) is not None]
    if len(available_mice) < 2:
        print(f"    skipped (need >= 2 mice with data, got {len(available_mice)})", flush=True)
        return per_fold, cm_payload

    for test_mouse in available_mice:
        candidate_train_mice = [m for m in available_mice if m != test_mouse]
        print(
            f"\n    test={test_mouse[-20:]} train_pool={len(candidate_train_mice)} ...",
            end=" ",
            flush=True,
        )

        try:
            test_data = mouse_data[test_mouse]
            test_labels_set = set(np.unique(test_data["labels"]).tolist())
            train_label_union: set = set()
            for mn in candidate_train_mice:
                train_label_union.update(np.unique(mouse_data[mn]["labels"]).tolist())

            shared_labels = sorted(test_labels_set & train_label_union)
            if len(shared_labels) < 2:
                print(f"skipped (shared_labels={len(shared_labels)} < 2)")
                continue

            encoder = LabelEncoder().fit(shared_labels)

            # Test set
            test_mask = np.isin(test_data["labels"], shared_labels)
            if not np.any(test_mask):
                print("skipped (no test samples)")
                continue
            test_x_vec = np.asarray(test_data["x_vec"][test_mask], dtype=np.float64)
            test_y = encoder.transform(test_data["labels"][test_mask])
            test_grid_paths = [test_data["grid_paths"][i] for i in np.where(test_mask)[0]]
            test_grid_frames = np.asarray(test_data["grid_frames"][test_mask], dtype=np.int64)

            # Train set
            train_x_parts: List[np.ndarray] = []
            train_y_parts: List[np.ndarray] = []
            train_grid_paths: List[Any] = []
            train_grid_frames_list: List[int] = []
            active_train_mice: List[str] = []

            for mn in candidate_train_mice:
                td = mouse_data[mn]
                mask = np.isin(td["labels"], shared_labels)
                if not np.any(mask):
                    continue
                active_train_mice.append(mn)
                train_x_parts.append(np.asarray(td["x_vec"][mask], dtype=np.float64))
                train_y_parts.append(encoder.transform(td["labels"][mask]))
                idxs = np.where(mask)[0]
                train_grid_paths.extend([td["grid_paths"][i] for i in idxs])
                train_grid_frames_list.extend(
                    np.asarray(td["grid_frames"][mask], dtype=np.int64).tolist()
                )

            if not train_x_parts:
                print("skipped (no training samples)")
                continue

            train_x_vec = np.concatenate(train_x_parts, axis=0)
            train_y = np.concatenate(train_y_parts, axis=0)
            train_grid_frames = np.asarray(train_grid_frames_list, dtype=np.int64)

            if len(np.unique(train_y)) < 2 or len(np.unique(test_y)) < 2:
                print("skipped (collapsed class)")
                continue

            # Scale vector features (same as non-shuffle script)
            from sklearn.preprocessing import StandardScaler
            scaler = StandardScaler().fit(train_x_vec)
            x_train_scaled = scaler.transform(train_x_vec)
            x_test_scaled = scaler.transform(test_x_vec)
            cnn1d_channels, cnn1d_seq_len = infer_cnn1d_shape(train_x_vec.shape[1], clip_frames)

            model_metrics: Dict[str, Dict[str, float]] = {}
            model_preds: Dict[str, np.ndarray] = {}

            if "logreg" in model_order:
                logreg_metrics, logreg_pred = train_eval_logreg(
                    x_train_scaled, train_y, x_test_scaled, test_y
                )
                model_metrics["logreg"] = logreg_metrics
                model_preds["logreg"] = logreg_pred

            if "mlp" in model_order:
                mlp_metrics, mlp_pred = train_eval_nn(
                    make_model=lambda: MLP(
                        n_classes=len(shared_labels), input_dim=train_x_vec.shape[1]
                    ),
                    train_ds=VectorDataset(x_train_scaled, train_y),
                    y_train=train_y,
                    test_ds=VectorDataset(x_test_scaled, test_y),
                    test_y=test_y,
                    epochs=epochs_mlp,
                    lr=lr_vec,
                    batch_size=batch_size_vec,
                    patience=early_stop_patience,
                    weight_decay=weight_decay,
                    device=device,
                    num_workers=num_workers_dl,
                    seed=seed,
                )
                model_metrics["mlp"] = mlp_metrics
                model_preds["mlp"] = mlp_pred

            if "cnn1d" in model_order:
                cnn1d_metrics, cnn1d_pred = train_eval_nn(
                    make_model=lambda: CNN1D(
                        n_classes=len(shared_labels),
                        in_channels=cnn1d_channels,
                        seq_len=cnn1d_seq_len,
                    ),
                    train_ds=VectorDataset(x_train_scaled, train_y),
                    y_train=train_y,
                    test_ds=VectorDataset(x_test_scaled, test_y),
                    test_y=test_y,
                    epochs=epochs_cnn1d,
                    lr=lr_vec,
                    batch_size=batch_size_vec,
                    patience=early_stop_patience,
                    weight_decay=weight_decay,
                    device=device,
                    num_workers=num_workers_dl,
                    seed=seed,
                )
                model_metrics["cnn1d"] = cnn1d_metrics
                model_preds["cnn1d"] = cnn1d_pred

            if "cnn3d_raw" in model_order:
                train_grid_ds_raw = GridTrialDataset(
                    grid_paths=train_grid_paths,
                    y=train_y,
                    valid_frames=train_grid_frames,
                    clip_frames=int(clip_frames),
                    normalize_by_trial=False,
                )
                test_grid_ds_raw = GridTrialDataset(
                    grid_paths=test_grid_paths,
                    y=test_y,
                    valid_frames=test_grid_frames,
                    clip_frames=int(clip_frames),
                    normalize_by_trial=False,
                )
                cnn3d_raw_metrics, cnn3d_raw_pred = train_eval_nn(
                    make_model=lambda: CNN3D(
                        n_classes=len(shared_labels),
                        in_channels=test_data["in_channels"],
                    ),
                    train_ds=train_grid_ds_raw,
                    y_train=train_y,
                    test_ds=test_grid_ds_raw,
                    test_y=test_y,
                    epochs=epochs_cnn3d,
                    lr=lr_cnn3d,
                    batch_size=batch_size_grid,
                    patience=early_stop_patience,
                    weight_decay=weight_decay,
                    device=device,
                    num_workers=num_workers_dl,
                    seed=seed,
                )
                model_metrics["cnn3d_raw"] = cnn3d_raw_metrics
                model_preds["cnn3d_raw"] = cnn3d_raw_pred

            if "cnn3d_norm" in model_order:
                train_grid_ds_norm = GridTrialDataset(
                    grid_paths=train_grid_paths,
                    y=train_y,
                    valid_frames=train_grid_frames,
                    clip_frames=int(clip_frames),
                    normalize_by_trial=True,
                )
                test_grid_ds_norm = GridTrialDataset(
                    grid_paths=test_grid_paths,
                    y=test_y,
                    valid_frames=test_grid_frames,
                    clip_frames=int(clip_frames),
                    normalize_by_trial=True,
                )
                cnn3d_norm_metrics, cnn3d_norm_pred = train_eval_nn(
                    make_model=lambda: CNN3D(
                        n_classes=len(shared_labels),
                        in_channels=test_data["in_channels"],
                    ),
                    train_ds=train_grid_ds_norm,
                    y_train=train_y,
                    test_ds=test_grid_ds_norm,
                    test_y=test_y,
                    epochs=epochs_cnn3d,
                    lr=lr_cnn3d,
                    batch_size=batch_size_grid,
                    patience=early_stop_patience,
                    weight_decay=weight_decay,
                    device=device,
                    num_workers=num_workers_dl,
                    seed=seed,
                )
                model_metrics["cnn3d_norm"] = cnn3d_norm_metrics
                model_preds["cnn3d_norm"] = cnn3d_norm_pred

            if not model_metrics:
                print("skipped (no models executed)")
                continue

            best_model = max(model_metrics.keys(), key=lambda mk: model_metrics[mk]["macro_f1"])

            per_fold[test_mouse] = {
                "shuffle_id": shuffle_id,
                "test_mouse": test_mouse,
                "train_mice": active_train_mice,
                "n_train_mice": len(active_train_mice),
                "n_train_trials": int(len(train_y)),
                "n_test_trials": int(len(test_y)),
                "n_features": int(train_x_vec.shape[1]),
                "clip_frames": int(clip_frames),
                "shared_labels": shared_labels,
                "n_classes": int(len(shared_labels)),
                "cnn1d_channels": int(cnn1d_channels),
                "cnn1d_seq_len": int(cnn1d_seq_len),
                "best_model": best_model,
                "models": model_metrics,
            }

            model_cms = {
                mk: confusion_matrix(
                    test_y,
                    model_preds[mk],
                    labels=np.arange(len(shared_labels)),
                )
                for mk in model_metrics.keys()
            }
            cm_payload[test_mouse] = {
                "labels": shared_labels,
                "cms": model_cms,
                "best_model": best_model,
            }

            f1 = max(model_metrics[mk]["macro_f1"] for mk in model_metrics.keys())
            print(f"ok (best={best_model}, f1={f1:.3f})")

        except Exception as exc:
            print(f"FAILED: {exc}")
            traceback.print_exc()

    return per_fold, cm_payload


def _save_lomo_summary_figures_for_group(
    *,
    group_name: str,
    per_fold_group: Dict[str, Dict[str, Any]],
    confusion_group: Dict[str, Dict[str, Any]],
    figures_dir: Path,
    method: str,
    grid_mode_label: str,
    model_order: List[str],
) -> List[Path]:
    """Save cross-mouse LOMO figure set for one group."""
    model_titles = {mk: MODEL_TITLES[mk] for mk in model_order}
    colors = {mk: MODEL_COLORS[mk] for mk in model_order}
    n_models = len(model_order)
    width = min(0.8 / max(n_models, 1), 0.22)
    offsets = {
        mk: (idx - (n_models - 1) / 2.0) * width
        for idx, mk in enumerate(model_order)
    }

    fold_order = sorted(per_fold_group.keys())
    if not fold_order:
        return []

    written: List[Path] = []

    # Figure 1: per-test-mouse bars
    fig, (ax_f1, ax_acc) = plt.subplots(
        2,
        1,
        figsize=(max(9, len(fold_order) * 1.3), 9.0),
        sharex=True,
    )
    x = np.arange(len(fold_order))

    for mk in model_order:
        vals_f1 = [float(per_fold_group[m]["models"][mk]["macro_f1"]) for m in fold_order]
        vals_acc = [float(per_fold_group[m]["models"][mk]["accuracy"]) for m in fold_order]
        ax_f1.bar(x + offsets[mk], vals_f1, width, label=model_titles[mk], alpha=0.85, color=colors[mk])
        ax_acc.bar(x + offsets[mk], vals_acc, width, label=model_titles[mk], alpha=0.85, color=colors[mk])

    ax_f1.set_ylim(0, 1.05)
    ax_f1.set_ylabel("Macro-F1")
    ax_f1.set_title(
        f"Cross-mouse LOMO macro-F1 by test mouse ({method}, {grid_mode_label}) - {group_name}"
    )
    ax_f1.grid(axis="y", alpha=0.25)
    ax_f1.legend(loc="upper right", ncol=min(3, n_models), fontsize=8)

    ax_acc.set_xticks(x)
    ax_acc.set_xticklabels([_short_mouse_name(m) for m in fold_order], rotation=30, ha="right", fontsize=8)
    ax_acc.set_ylim(0, 1.05)
    ax_acc.set_ylabel("Accuracy")
    ax_acc.set_title(
        f"Cross-mouse LOMO accuracy by test mouse ({method}, {grid_mode_label}) - {group_name}"
    )
    ax_acc.grid(axis="y", alpha=0.25)

    fig.tight_layout()
    fig1 = figures_dir / f"01_lomo_macro_f1_by_test_mouse_{group_name}.png"
    fig.savefig(fig1, dpi=300, bbox_inches="tight")
    plt.close(fig)
    written.append(fig1)

    # Figure 2: mean across test mice
    fig, axes = plt.subplots(1, 2, figsize=(10, 4.6))
    mean_acc = [np.mean([per_fold_group[m]["models"][mk]["accuracy"] for m in fold_order]) for mk in model_order]
    mean_f1 = [np.mean([per_fold_group[m]["models"][mk]["macro_f1"] for m in fold_order]) for mk in model_order]
    std_acc = [np.std([per_fold_group[m]["models"][mk]["accuracy"] for m in fold_order]) for mk in model_order]
    std_f1 = [np.std([per_fold_group[m]["models"][mk]["macro_f1"] for m in fold_order]) for mk in model_order]
    xi = np.arange(len(model_order))

    axes[0].bar(xi, mean_acc, yerr=std_acc, capsize=4, color=[colors[m] for m in model_order], alpha=0.85)
    axes[0].set_xticks(xi)
    axes[0].set_xticklabels([model_titles[m] for m in model_order], rotation=25, ha="right", fontsize=8)
    axes[0].set_ylim(0, 1.05)
    axes[0].set_ylabel("Accuracy")
    axes[0].set_title(f"Mean accuracy across test mice ({group_name}, {grid_mode_label})")
    axes[0].grid(axis="y", alpha=0.25)

    axes[1].bar(xi, mean_f1, yerr=std_f1, capsize=4, color=[colors[m] for m in model_order], alpha=0.85)
    axes[1].set_xticks(xi)
    axes[1].set_xticklabels([model_titles[m] for m in model_order], rotation=25, ha="right", fontsize=8)
    axes[1].set_ylim(0, 1.05)
    axes[1].set_ylabel("Macro-F1")
    axes[1].set_title(f"Mean macro-F1 across test mice ({group_name}, {grid_mode_label})")
    axes[1].grid(axis="y", alpha=0.25)

    fig.tight_layout()
    fig2 = figures_dir / f"02_lomo_mean_scores_{group_name}.png"
    fig.savefig(fig2, dpi=300, bbox_inches="tight")
    plt.close(fig)
    written.append(fig2)

    # Figure 3: normalized confusion matrices (rows=test mice, cols=models)
    n_mice = len(fold_order)
    n_models = len(model_order)
    fig, axes = plt.subplots(
        n_mice,
        n_models,
        figsize=(4.4 * n_models, 3.8 * n_mice),
        squeeze=False,
    )
    for row_idx, mouse_name in enumerate(fold_order):
        payload = confusion_group[mouse_name]
        labels_order = payload["labels"]
        best_model = payload["best_model"]
        for col_idx, mk in enumerate(model_order):
            ax = axes[row_idx][col_idx]
            cm_arr = np.asarray(payload["cms"][mk], dtype=float)
            row_sums = cm_arr.sum(axis=1, keepdims=True)
            cm_norm = np.divide(cm_arr, row_sums, out=np.zeros_like(cm_arr), where=row_sums != 0)
            ConfusionMatrixDisplay(cm_norm, display_labels=labels_order).plot(
                ax=ax,
                cmap="Blues",
                colorbar=False,
                values_format=".2f",
            )
            title = f"{_short_mouse_name(mouse_name)}\\n{model_titles[mk]}"
            if mk == best_model:
                title += " ★"
            ax.set_title(title, fontsize=7)

    fig.suptitle(
        f"Normalized confusion matrices ({group_name}, {grid_mode_label}) - all classifiers per test mouse (★ = best)",
        fontsize=11,
    )
    fig.tight_layout()
    fig3 = figures_dir / f"03_all_classifier_confusion_matrices_{group_name}.png"
    fig.savefig(fig3, dpi=300, bbox_inches="tight")
    plt.close(fig)
    written.append(fig3)

    return written


