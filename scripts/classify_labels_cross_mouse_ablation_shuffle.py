# ---------------------------------------------------------------------------

def main() -> None:
    parser = _build_parser()
    args = parser.parse_args()

    model_order = _resolve_models(args.models)
    zz_folder = args.zz_folder or _build_zz_folder(args.p_active, args.per_trial_thresh)

    # Discover / validate mice
    discovered = _discover_mice(args.data_root)
    eligible_mice = validate_mice_for_ablation(
        args.mice, args.data_root, args.meta_root, discovered
    )
    if not eligible_mice:
        print("ERROR: No eligible mice found.", file=sys.stderr)
        sys.exit(1)
    if len(eligible_mice) < 2:
        print("ERROR: Cross-mouse analysis requires at least 2 eligible mice.", file=sys.stderr)
        sys.exit(1)

    # Determine clip frames
    if args.clip_frames is not None:
        global_clip = int(args.clip_frames)
    else:
        print("Pre-scanning valid_frames for global clip ...", flush=True)
        min_list = []
        for mn in eligible_mice:
            try:
                _, _, _, vf = load_labelled_barcodes(
                    args.data_root, args.meta_root, mn, zz_folder, max_trials=args.max_trials
                )
                if len(vf) > 0:
                    min_list.append(int(vf.min()))
            except Exception:
                pass
        if not min_list:
            print("ERROR: Could not determine global clip_frames.", file=sys.stderr)
            sys.exit(1)
        global_clip = min(min_list)
        print(f"  global_clip_frames = {global_clip}", flush=True)

    # ---- PREFLIGHT CHECKS --------------------------------------------------
    print("\nRunning preflight checks ...", flush=True)
    sampled_ids = _preflight_check(
        eligible_mice=eligible_mice,
        data_root=args.data_root,
        cache_dir=args.cache_dir,
        shuffle_type=args.shuffle_type,
        method=args.vectorization_method,
        p_active=args.p_active,
        per_trial_thresh=args.per_trial_thresh,
        clip_frames=global_clip,
        different_shuffle_per_trial=args.different_shuffle_per_trial,
        n_shuffles=args.n_shuffles,
        max_trials=args.max_trials,
        seed=args.seed,
    )
    print(f"Preflight PASSED. Sampled shuffle IDs (same for all mice): {sampled_ids}", flush=True)
    # -------------------------------------------------------------------------

    mode_token = _shuffle_mode_token(args.different_shuffle_per_trial)
    output_dir = args.output_folder / mode_token
    output_dir.mkdir(parents=True, exist_ok=True)
    figures_dir = output_dir / "figures"
    logs_dir = output_dir / "logs"
    figures_dir.mkdir(parents=True, exist_ok=True)
    logs_dir.mkdir(parents=True, exist_ok=True)
    log_path = logs_dir / "run.log"

    device = _resolve_device(args.device)
    _set_seed(args.seed)

    fs = _FakeState(cache_dir=args.cache_dir, data_root=args.data_root)

    with open(log_path, "w", encoding="utf-8") as log_fp, \
         redirect_stdout(log_fp), redirect_stderr(log_fp):

        print("=" * 90)
        print("Cross-Mouse LOMO Classification on Shuffled Vectorizations")
        print(f"Timestamp: {datetime.now().isoformat(timespec='seconds')}")
        print("=" * 90)
        print(f"  output_folder        : {output_dir}")
        print(f"  shuffle_type         : {args.shuffle_type}")
        print(f"  n_shuffles (sampled) : {args.n_shuffles}")
        print(f"  sampled_ids          : {sampled_ids}")
        print(f"  shuffle_mode         : {mode_token}")
        print(f"  method               : {args.vectorization_method}")
        print(f"  p_active             : {args.p_active}")
        print(f"  clip_frames          : {global_clip}")
        print(f"  models               : {model_order}")
        print(f"  device               : {device}")
        print(f"  seed                 : {args.seed}")

        grid_mode_label = _grid_mode_label(model_order)

        all_results: Dict[int, Dict[str, Any]] = {}  # {shuffle_id: {test_mouse: result}}
        all_cms: Dict[int, Dict[str, Any]] = {}

        for shuffle_id in sampled_ids:
            print(f"\n{'='*80}")
            print(f"Shuffle ID: {shuffle_id}")
            print(f"{'='*80}")

            # Load all mice data for this shuffle
            mouse_data: Dict[str, Optional[Dict[str, Any]]] = {}
            for mouse_name in eligible_mice:
                base_stem = build_vectorization_cache_stem(
                    mouse_name=mouse_name,
                    method=args.vectorization_method,
                    p_active=args.p_active,
                    per_trial_thresh=args.per_trial_thresh,
                    clip_frames=global_clip,
                )
                mcdir = _resolve_mouse_cache_dir(fs, mouse_name)
                shuffle_stem = _shuffle_cache_stem_with_mode(
                    base_stem,
                    args.shuffle_type,
                    shuffle_id,
                    args.different_shuffle_per_trial,
                )
                cache_path = mcdir / f"{shuffle_stem}.npz"

                print(f"  Loading {mouse_name[-30:]} ...", end=" ", flush=True)
                try:
                    data = _load_mouse_shuffle_data(
                        mouse_name=mouse_name,
                        shuffle_id=shuffle_id,
                        cache_path=cache_path,
                        clip_frames=global_clip,
                    )
                    mouse_data[mouse_name] = data
                    status = "ok" if data is not None else "skipped (insufficient labels)"
                    print(status)
                except Exception as exc:
                    print(f"FAILED: {exc}")
                    traceback.print_exc()
                    mouse_data[mouse_name] = None

            # Run LOMO
            per_fold, cm_payload = _run_cross_mouse_one_shuffle(
                shuffle_id=shuffle_id,
                eligible_mice=eligible_mice,
                mouse_data=mouse_data,
                clip_frames=global_clip,
                model_order=model_order,
                device=device,
                epochs_mlp=args.epochs_mlp,
                epochs_cnn1d=args.epochs_cnn1d,
                epochs_cnn3d=args.epochs_cnn3d,
                lr_vec=args.lr_vec,
                lr_cnn3d=args.lr_cnn3d,
                batch_size_vec=args.batch_size_vec,
                batch_size_grid=args.batch_size_grid,
                weight_decay=args.weight_decay,
                early_stop_patience=args.early_stop_patience,
                num_workers_dl=args.num_workers_dl,
                seed=args.seed,
            )
            all_results[shuffle_id] = per_fold
            all_cms[shuffle_id] = cm_payload
            gc.collect()

        # ---- Save figures ---------------------------------------------------
        print("\nSaving LOMO summary figures (per shuffle + average across shuffles) ...", flush=True)
        written_figures: List[Path] = []

        # Per-shuffle figure sets
        for shuffle_id in sorted(all_results.keys()):
            per_fold_group = all_results.get(shuffle_id, {})
            confusion_group = all_cms.get(shuffle_id, {})
            if per_fold_group and confusion_group:
                gname = f"shuffle{int(shuffle_id):04d}"
                written_figures.extend(
                    _save_lomo_summary_figures_for_group(
                        group_name=gname,
                        per_fold_group=per_fold_group,
                        confusion_group=confusion_group,
                        figures_dir=figures_dir,
                        method=args.vectorization_method,
                        grid_mode_label=grid_mode_label,
                        model_order=model_order,
                    )
                )

        # Average-across-shuffles figure set
        avg_per_fold: Dict[str, Dict[str, Any]] = {}
        avg_cms: Dict[str, Dict[str, Any]] = {}
        test_mice_present = sorted({tm for fold in all_results.values() for tm in fold.keys()})
        for test_mouse in test_mice_present:
            available_shuffle_ids = [sid for sid, fold in all_results.items() if test_mouse in fold]
            if not available_shuffle_ids:
                continue

            _all_model_keys = list(model_order)
            avg_models = {
                mk: {
                    "accuracy": float(np.mean([float(all_results[sid][test_mouse]["models"][mk]["accuracy"]) for sid in available_shuffle_ids])),
                    "macro_f1": float(np.mean([float(all_results[sid][test_mouse]["models"][mk]["macro_f1"]) for sid in available_shuffle_ids])),
                    "std_accuracy": float(np.std([float(all_results[sid][test_mouse]["models"][mk]["accuracy"]) for sid in available_shuffle_ids])),
                    "std_macro_f1": float(np.std([float(all_results[sid][test_mouse]["models"][mk]["macro_f1"]) for sid in available_shuffle_ids])),
                }
                for mk in _all_model_keys
            }
            best_model = max(_all_model_keys, key=lambda m: avg_models[m]["macro_f1"])

            first_row = all_results[available_shuffle_ids[0]][test_mouse]
            avg_per_fold[test_mouse] = {
                "shuffle_id": "avg",
                "test_mouse": test_mouse,
                "train_mice": first_row["train_mice"],
                "n_train_mice": int(first_row["n_train_mice"]),
                "n_train_trials": int(first_row["n_train_trials"]),
                "n_test_trials": int(first_row["n_test_trials"]),
                "n_features": int(first_row["n_features"]),
                "clip_frames": int(first_row["clip_frames"]),
                "shared_labels": list(first_row["shared_labels"]),
                "n_classes": int(first_row["n_classes"]),
                "best_model": best_model,
                "models": {
                    mk: {"accuracy": avg_models[mk]["accuracy"], "macro_f1": avg_models[mk]["macro_f1"]}
                    for mk in _all_model_keys
                },
            }

            cm_available_ids = [sid for sid, cms in all_cms.items() if test_mouse in cms]
            if cm_available_ids:
                labels_order = list(all_cms[cm_available_ids[0]][test_mouse]["labels"])
                avg_cms[test_mouse] = {
                    "labels": labels_order,
                    "cms": {
                        mk: np.mean(
                            np.stack([np.asarray(all_cms[sid][test_mouse]["cms"][mk], dtype=float) for sid in cm_available_ids], axis=0),
                            axis=0,
                        )
                        for mk in _all_model_keys
                    },
                    "best_model": best_model,
                }

        if avg_per_fold and avg_cms:
            written_figures.extend(
                _save_lomo_summary_figures_for_group(
                    group_name="avg_across_shuffles",
                    per_fold_group=avg_per_fold,
                    confusion_group=avg_cms,
                    figures_dir=figures_dir,
                    method=args.vectorization_method,
                    grid_mode_label=grid_mode_label,
                    model_order=model_order,
                )
            )

        # ---- Save JSON / CSV ------------------------------------------------
        json_path = output_dir / "cross_mouse_ablation_shuffle_metrics.json"
        csv_path = output_dir / "cross_mouse_ablation_shuffle_metrics.csv"

        payload_json = {
            "method": args.vectorization_method,
            "p_active": args.p_active,
            "shuffle_type": args.shuffle_type,
            "different_shuffle_per_trial": args.different_shuffle_per_trial,
            "shuffle_mode": mode_token,
            "models": model_order,
            "n_shuffles_requested": args.n_shuffles,
            "seed": args.seed,
            "eligible_mice": eligible_mice,
            "sampled_shuffle_ids": sampled_ids,
            "per_shuffle": all_results,
            "figures": [str(p) for p in written_figures],
        }
        with open(json_path, "w", encoding="utf-8") as fp:
            json.dump(payload_json, fp, indent=2)
        print(f"Wrote JSON: {json_path}")

        with open(csv_path, "w", encoding="utf-8", newline="") as fp:
            writer = csv.writer(fp)
            writer.writerow([
                "shuffle_mode",
                "different_shuffle_per_trial",
                "shuffle_id", "test_mouse", "model",
                "n_train_trials", "n_test_trials",
                "accuracy", "macro_f1",
                "best_model",
            ])
            for shuffle_id, fold_results in all_results.items():
                for test_mouse, row in fold_results.items():
                    for mk in model_order:
                        mr = row["models"][mk]
                        writer.writerow([
                            mode_token,
                            args.different_shuffle_per_trial,
                            shuffle_id,
                            test_mouse,
                            mk,
                            row["n_train_trials"],
                            row["n_test_trials"],
                            mr["accuracy"],
                            mr["macro_f1"],
                            row["best_model"],
                        ])
        print(f"Wrote CSV : {csv_path}")

        print("\n" + "=" * 90)
        print("SUCCESS")
        print("=" * 90)

    print(f"Done. Results: {output_dir}", flush=True)


if __name__ == "__main__":
    main()
