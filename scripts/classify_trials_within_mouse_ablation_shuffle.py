def _resolve_device(name: str) -> torch.device:
    if name == "cpu":
        return torch.device("cpu")
    if name == "cuda":
        if not torch.cuda.is_available():
            raise RuntimeError("--device=cuda requested but CUDA is not available")
        return torch.device("cuda")
    if torch.cuda.is_available():
        return torch.device("cuda")
    return torch.device("cpu")


def _set_seed(seed: int) -> None:
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)


# ---------------------------------------------------------------------------
# Preflight validation
# ---------------------------------------------------------------------------

@dataclass
class _FakeState:
    cache_dir: Optional[Path]
    data_root: Path


def _preflight_check(
    eligible_mice: List[str],
    data_root: Path,
    cache_dir: Optional[Path],
    shuffle_type: str,
    method: str,
    p_active: int,
    per_trial_thresh: bool,
    clip_frames: int,
    n_shuffles: int,
    max_trials: Optional[int],
    seed: int,
) -> Dict[str, List[int]]:
    """Validate shuffle availability and return sampled IDs per mouse.

    Raises
    ------
    SystemExit
        With a clear, per-mouse error message when any check fails.
    """
    fs = _FakeState(cache_dir=cache_dir, data_root=data_root)
    rng = np.random.default_rng(seed)

    errors: List[str] = []
    sampled: Dict[str, List[int]] = {}

    for mouse_name in eligible_mice:
        mcdir = _resolve_mouse_cache_dir(fs, mouse_name)
        base_stem = build_vectorization_cache_stem(
            mouse_name=mouse_name,
            method=method,
            p_active=p_active,
            per_trial_thresh=per_trial_thresh,
            clip_frames=clip_frames,
        )
        available = _existing_shuffle_ids(mcdir, base_stem, shuffle_type)

        # Check 1: Enough shuffles available?
        if len(available) < n_shuffles:
            errors.append(
                f"  {mouse_name}: needs {n_shuffles} shuffles but only "
                f"{len(available)} available (ids={available}). "
                f"Missing: {n_shuffles - len(available)}"
            )
            continue

        # Check 2: Manifest max_trials consistency
        if max_trials is not None:
            manifest = _load_manifest(mcdir)
            mkey = _manifest_key(shuffle_type, method, p_active, per_trial_thresh, clip_frames)
            entry = manifest.get(mkey, {})
            stored_max = entry.get("max_trials_used", None)
            # None stored means no limit was used during generation (= all trials)
            # A stored value < max_trials means generation used fewer trials than required
            if stored_max is not None and stored_max < max_trials:
                errors.append(
                    f"  {mouse_name}: manifest records max_trials_used={stored_max} "
                    f"but --max-trials={max_trials} requested. "
                    f"Regenerate shuffles with at least max_trials={max_trials}."
                )
                continue

        # Sample N from available (reproducible per mouse: extend seed with mouse hash)
        mouse_seed = int(rng.integers(0, 2**31))
        mouse_rng = np.random.default_rng(mouse_seed)
        chosen = sorted(mouse_rng.choice(available, size=n_shuffles, replace=False).tolist())
        sampled[mouse_name] = chosen

    if errors:
        print("\nPREFLIGHT ERRORS — cannot proceed:", flush=True)
        for e in errors:
            print(e, flush=True)
        sys.exit(1)

    return sampled


# ---------------------------------------------------------------------------
# Within-mouse CV for one shuffle
# ---------------------------------------------------------------------------

def _run_within_mouse_one_shuffle(
    *,
    mouse_name: str,
    shuffle_id: int,
    cache_path: Path,
    data_root: Path,
    meta_root: Path,
    zz_folder: str,
    grid_subdir: str,
    max_trials: Optional[int],
    clip_frames: int,
    device: torch.device,
    epochs_cnn3d: int,
    lr_cnn3d: float,
    batch_size_grid: int,
    weight_decay: float,
    early_stop_patience: int,
    num_workers_dl: int,
    seed: int,
) -> Tuple[Optional[Dict[str, Any]], Optional[Dict[str, Any]]]:
    """Return (result_dict, cm_payload) or (None, None) on skip."""

    # Load cached vectorization
    cached = load_vectorization_cache(cache_path)
    x_vec = np.nan_to_num(np.asarray(
        cached["features"] if "features" in cached else cached["X"]
    ))
    labels = np.asarray(cached["labels"])
    trial_ids_cache = np.asarray(cached.get("trial_ids", np.arange(len(labels))))
    valid_frames_cache = np.asarray(cached.get("valid_frames", np.full(len(labels), clip_frames)))

    if len(np.unique(labels)) < 2:
        print(f"    skipped (only 1 class)", flush=True)
        return None, None

    # Load grid paths for CNN3D
    grid_paths, _, grid_trial_ids, grid_valid_frames = load_labelled_grid_paths(
        data_root, meta_root, mouse_name, grid_subdir=grid_subdir
    )
    grid_idx = {int(tid): i for i, tid in enumerate(grid_trial_ids)}
    # Align trial IDs between cached vec and grids
    common = [tid for tid in trial_ids_cache if int(tid) in grid_idx]
    if not common:
        print(f"    skipped (no overlapping trial IDs with grids)", flush=True)
        return None, None

    vec_idx_map = {int(tid): i for i, tid in enumerate(trial_ids_cache)}
    vec_take = np.array([vec_idx_map[int(tid)] for tid in common], dtype=np.int64)
    grid_take = np.array([grid_idx[int(tid)] for tid in common], dtype=np.int64)

    x_vec_aligned = x_vec[vec_take]
    labels_aligned = labels[vec_take]
    grid_paths_aligned = [grid_paths[i] for i in grid_take]
    gf_aligned = grid_valid_frames[grid_take]

    le = LabelEncoder().fit(labels_aligned)
    y_int = le.transform(labels_aligned)
    class_labels = list(le.classes_)

    n_splits = min(5, int(np.bincount(y_int).min()))
    if n_splits < 2:
        print(f"    skipped (min class count < 2)", flush=True)
        return None, None

    cv = StratifiedKFold(n_splits=n_splits, shuffle=True, random_state=seed)
    splits = list(cv.split(np.zeros(len(y_int)), y_int))

    # LogReg
    logreg_metrics, logreg_pred = run_logreg_cv(x_vec_aligned, y_int, splits)

    # 3D-CNN
    grid_dataset = GridTrialDataset(
        grid_paths=grid_paths_aligned,
        y=y_int,
        valid_frames=gf_aligned,
        clip_frames=int(clip_frames),
    )
    cnn3d_metrics, cnn3d_pred = run_nn_cv(
        make_model=lambda: CNN3D(
            n_classes=len(class_labels),
            in_channels=grid_dataset.in_channels,
        ),
        train_dataset_builder=lambda tr_idx, val_idx: (
            Subset(grid_dataset, tr_idx), Subset(grid_dataset, val_idx)
        ),
        y_int=y_int,
        splits=splits,
        epochs=epochs_cnn3d,
        lr=lr_cnn3d,
        batch_size=batch_size_grid,
        patience=early_stop_patience,
        weight_decay=weight_decay,
        device=device,
        num_workers=num_workers_dl,
    )

    best_model = max(
        ["logreg", "cnn3d"],
        key=lambda m: {"logreg": logreg_metrics, "cnn3d": cnn3d_metrics}[m].get("mean_f1", 0),
    )

    result = {
        "shuffle_id": shuffle_id,
        "mouse": mouse_name,
        "n_trials": int(len(y_int)),
        "cv_folds": n_splits,
        "n_features": int(x_vec_aligned.shape[1]),
        "clip_frames": int(clip_frames),
        "class_labels": class_labels,
        "n_classes": len(class_labels),
        "best_model": best_model,
        "models": {
            "logreg": logreg_metrics,
            "cnn3d": cnn3d_metrics,
        },
    }

    model_cms = {
        mk: confusion_matrix(y_int, {"logreg": logreg_pred, "cnn3d": cnn3d_pred}[mk],
                             labels=np.arange(len(class_labels)))
        for mk in ["logreg", "cnn3d"]
    }
    cm_payload = {
        "labels": class_labels,
        "cms": model_cms,
        "best_model": best_model,
    }

    return result, cm_payload


