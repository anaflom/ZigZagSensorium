def main() -> None:
    parser = _build_parser()
    args = parser.parse_args()

    zz_folder = args.zz_folder or _build_zz_folder(args.p_active, args.per_trial_thresh)

    # Discover / validate mice
    discovered = _discover_mice(args.data_root)
    eligible_mice = validate_mice_for_ablation(
        args.mice, args.data_root, args.meta_root, discovered
    )
    if not eligible_mice:
        print("ERROR: No eligible mice found.", file=sys.stderr)
        sys.exit(1)

    # Determine clip frames
    if args.clip_frames is not None:
        global_clip = int(args.clip_frames)
    else:
        print("Pre-scanning valid_frames for global clip ...", flush=True)
        min_list = []
        for mn in eligible_mice:
            try:
                _, _, _, vf = load_labelled_barcodes(
                    args.data_root, args.meta_root, mn, zz_folder, max_trials=args.max_trials
                )
                if len(vf) > 0:
                    min_list.append(int(vf.min()))
            except Exception:
                pass
        if not min_list:
            print("ERROR: Could not determine global clip_frames.", file=sys.stderr)
            sys.exit(1)
        global_clip = min(min_list)
        print(f"  global_clip_frames = {global_clip}", flush=True)

    # ---- PREFLIGHT CHECKS --------------------------------------------------
    print("\nRunning preflight checks ...", flush=True)
    sampled_ids = _preflight_check(
        eligible_mice=eligible_mice,
        data_root=args.data_root,
        cache_dir=args.cache_dir,
        shuffle_type=args.shuffle_type,
        method=args.vectorization_method,
        p_active=args.p_active,
        per_trial_thresh=args.per_trial_thresh,
        clip_frames=global_clip,
        n_shuffles=args.n_shuffles,
        max_trials=args.max_trials,
        seed=args.seed,
    )
    print(f"Preflight PASSED. Sampled shuffle IDs per mouse:", flush=True)
    for mn, ids in sampled_ids.items():
        print(f"  {mn}: {ids}", flush=True)
    # -------------------------------------------------------------------------

    args.output_folder.mkdir(parents=True, exist_ok=True)
    figures_dir = args.output_folder / "figures"
    logs_dir = args.output_folder / "logs"
    figures_dir.mkdir(parents=True, exist_ok=True)
    logs_dir.mkdir(parents=True, exist_ok=True)
    log_path = logs_dir / "run.log"

    device = _resolve_device(args.device)
    _set_seed(args.seed)

    fs = _FakeState(cache_dir=args.cache_dir, data_root=args.data_root)

    with open(log_path, "w", encoding="utf-8") as log_fp, \
         redirect_stdout(log_fp), redirect_stderr(log_fp):

        print("=" * 90)
        print("Within-Mouse Classification on Shuffled Vectorizations")
        print(f"Timestamp: {datetime.now().isoformat(timespec='seconds')}")
        print("=" * 90)
        print(f"  output_folder        : {args.output_folder}")
        print(f"  shuffle_type         : {args.shuffle_type}")
        print(f"  n_shuffles (sampled) : {args.n_shuffles}")
        print(f"  method               : {args.vectorization_method}")
        print(f"  p_active             : {args.p_active}")
        print(f"  clip_frames          : {global_clip}")
        print(f"  device               : {device}")
        print(f"  seed                 : {args.seed}")
        print("\nSampled shuffle IDs:")
        for mn, ids in sampled_ids.items():
            print(f"  {mn}: {ids}")

        # Containers for results
        all_results: Dict[str, Dict[str, Any]] = {}  # {mouse: {shuffle_id: result}}
        all_cms: Dict[str, Dict[str, Any]] = {}

        for mouse_name in eligible_mice:
            print(f"\n{'='*70}")
            print(f"Mouse: {mouse_name}")
            all_results[mouse_name] = {}
            all_cms[mouse_name] = {}

            base_stem = build_vectorization_cache_stem(
                mouse_name=mouse_name,
                method=args.vectorization_method,
                p_active=args.p_active,
                per_trial_thresh=args.per_trial_thresh,
                clip_frames=global_clip,
            )
            mcdir = _resolve_mouse_cache_dir(fs, mouse_name)

            for shuffle_id in sampled_ids[mouse_name]:
                shuffle_stem = _shuffle_cache_stem(base_stem, args.shuffle_type, shuffle_id)
                cache_path = mcdir / f"{shuffle_stem}.npz"

                print(f"\n  [shuffle {shuffle_id}]", end=" ", flush=True)
                try:
                    result, cm_payload = _run_within_mouse_one_shuffle(
                        mouse_name=mouse_name,
                        shuffle_id=shuffle_id,
                        cache_path=cache_path,
                        data_root=args.data_root,
                        meta_root=args.meta_root,
                        zz_folder=zz_folder,
                        grid_subdir=args.grid_subdir,
                        max_trials=args.max_trials,
                        clip_frames=global_clip,
                        device=device,
                        epochs_cnn3d=args.epochs_cnn3d,
                        lr_cnn3d=args.lr_cnn3d,
                        batch_size_grid=args.batch_size_grid,
                        weight_decay=args.weight_decay,
                        early_stop_patience=args.early_stop_patience,
                        num_workers_dl=args.num_workers_dl,
                        seed=args.seed,
                    )
                    if result is not None:
                        all_results[mouse_name][shuffle_id] = result
                        all_cms[mouse_name][shuffle_id] = cm_payload
                        best = result["best_model"]
                        f1 = result["models"][best].get("mean_f1", 0)
                        print(f"ok (best={best}:{f1:.3f})")
                except Exception as exc:
                    print(f"FAILED: {exc}")
                    traceback.print_exc()

                gc.collect()

        # ---- Save figures ---------------------------------------------------
        print("\nSaving confusion matrix figures ...", flush=True)
        for mouse_name, shuffle_cms in all_cms.items():
            for shuffle_id, payload in shuffle_cms.items():
                if payload is None:
                    continue
                for model_key, cm_arr in payload["cms"].items():
                    fig, ax = plt.subplots(figsize=(6, 5))
                    im = ax.imshow(cm_arr, interpolation="nearest", cmap="Blues")
                    ax.set_title(f"{mouse_name}\nshuffle {shuffle_id} | {model_key}")
                    ax.set_xlabel("Predicted")
                    ax.set_ylabel("True")
                    tick_marks = np.arange(len(payload["labels"]))
                    ax.set_xticks(tick_marks)
                    ax.set_xticklabels(payload["labels"], rotation=45, ha="right")
                    ax.set_yticks(tick_marks)
                    ax.set_yticklabels(payload["labels"])
                    plt.colorbar(im, ax=ax)
                    plt.tight_layout()
                    short = mouse_name[-20:]
                    fname = figures_dir / f"cm_within_{short}_s{shuffle_id:04d}_{model_key}.png"
                    fig.savefig(fname, dpi=100, bbox_inches="tight")
                    plt.close(fig)

        # ---- Save JSON / CSV ------------------------------------------------
        json_path = args.output_folder / "within_mouse_ablation_shuffle_metrics.json"
        csv_path = args.output_folder / "within_mouse_ablation_shuffle_metrics.csv"

        payload_json = {
            "method": args.vectorization_method,
            "p_active": args.p_active,
            "shuffle_type": args.shuffle_type,
            "n_shuffles_requested": args.n_shuffles,
            "seed": args.seed,
            "eligible_mice": eligible_mice,
            "sampled_shuffle_ids": sampled_ids,
            "per_mouse": all_results,
        }
        with open(json_path, "w", encoding="utf-8") as fp:
            json.dump(payload_json, fp, indent=2)
        print(f"Wrote JSON: {json_path}")

        with open(csv_path, "w", encoding="utf-8", newline="") as fp:
            writer = csv.writer(fp)
            writer.writerow([
                "mouse", "shuffle_id", "model",
                "n_trials", "cv_folds",
                "mean_acc", "std_acc", "mean_f1", "std_f1",
                "best_model",
            ])
            for mouse_name, shuf_results in all_results.items():
                for shuffle_id, row in shuf_results.items():
                    for mk in ["logreg", "cnn3d"]:
                        mr = row["models"][mk]
                        writer.writerow([
                            mouse_name,
                            shuffle_id,
                            mk,
                            row["n_trials"],
                            row["cv_folds"],
                            mr.get("mean_acc", mr.get("accuracy")),
                            mr.get("std_acc", 0),
                            mr.get("mean_f1", mr.get("macro_f1")),
                            mr.get("std_f1", 0),
                            row["best_model"],
                        ])
        print(f"Wrote CSV : {csv_path}")

        print("\n" + "=" * 90)
        print("SUCCESS")
        print("=" * 90)

    print(f"Done. Results: {args.output_folder}", flush=True)


if __name__ == "__main__":
    main()
