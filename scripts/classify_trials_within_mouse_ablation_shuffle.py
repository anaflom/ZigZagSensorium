"""Within-mouse classification on pre-cached shuffled zigzag vectorizations.

Requires shuffles to be pre-generated by generate_trials_ablation_shuffle.py.

Preflight checks (all run before any training):
  1. Each mouse has at least --n-shuffles available randomizations for the
     requested shuffle-type / configuration.  Fails with an explicit message
     listing which mice have too few and how many are missing.
  2. Shuffle manifests indicate generation used at least --max-trials.
     Fails if any mouse/shuffle was generated with fewer trials than requested.
  3. Randomly samples exactly --n-shuffles IDs from each mouse's available
     pool (seeded by --seed for reproducibility).

Outputs per run folder:
  within_mouse_ablation_shuffle_metrics.json
  within_mouse_ablation_shuffle_metrics.csv
  figures/   (confusion matrices)
  logs/run.log

Usage examples
--------------
  python classify_trials_within_mouse_ablation_shuffle.py \\
         --output-folder results/ablation_shuffle/time/p30-per-trial/within_run1 \\
         --n-shuffles 5 --shuffle-type time

  python classify_trials_within_mouse_ablation_shuffle.py \\
         --output-folder results/ablation_shuffle/time/p30-per-trial/within_run2 \\
         --n-shuffles 10 --shuffle-type time --max-trials 200 --seed 0
"""

