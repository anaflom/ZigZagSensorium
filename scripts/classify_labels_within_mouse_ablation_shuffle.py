def _run_within_mouse_one_shuffle(
    *,
    mouse_name: str,
    shuffle_id: int,
    cache_path: Path,
    clip_frames: int,
    n_splits: int,
    device: torch.device,
    epochs_mlp: int,
    epochs_cnn1d: int,
    epochs_cnn3d: int,
    lr_vec: float,
    lr_cnn3d: float,
    batch_size_vec: int,
    batch_size_grid: int,
    normalize_grids: bool,
    weight_decay: float,
    early_stop_patience: int,
    num_workers_dl: int,
    seed: int,
) -> Tuple[Optional[Dict[str, Any]], Optional[Dict[str, Any]]]:
    """Return (result_dict, cm_payload) or (None, None) on skip.

    Uses the same four-model CV logic as classify_labels_within_mouse_ablation.py.
    The only difference is that data is loaded from the shuffled feature/grid caches.
    """
    from sklearn.preprocessing import StandardScaler

    model_order = ["logreg", "mlp", "cnn1d", "cnn3d"]

    # Load cached shuffled vectorization
    cached = load_vectorization_cache(cache_path)
    x_vec = np.nan_to_num(np.asarray(
        cached["features"] if "features" in cached else cached["X"]
    ))
    labels = np.asarray(cached["labels"])
    trial_ids_cache = np.asarray(cached.get("trial_ids", np.arange(len(labels))))
    valid_frames_cache = np.asarray(cached.get("valid_frames", np.full(len(labels), clip_frames)))

    if len(np.unique(labels)) < 2:
        print(f"    skipped (only 1 class)", flush=True)
        return None, None

    # Load shuffled grid paths saved by generate_trials_ablation_shuffle.py
    try:
        grid_paths = load_shuffled_grid_cache_paths(cache_path, trial_ids_cache.tolist())
    except FileNotFoundError as exc:
        print(f"    skipped (shuffled grid cache missing: {exc})", flush=True)
        return None, None

    le = LabelEncoder().fit(labels)
    y_int = le.transform(labels)
    class_labels = list(le.classes_)

    folds = min(int(n_splits), int(np.bincount(y_int).min()))
    if folds < 2:
        print(f"    skipped (min class count < 2)", flush=True)
        return None, None

    cv = StratifiedKFold(n_splits=folds, shuffle=True, random_state=seed)
    splits = list(cv.split(np.zeros(len(y_int)), y_int))

    clip_used = int(clip_frames)

    # LogReg (uses internal per-fold scaling)
    logreg_metrics, logreg_pred = run_logreg_cv(x_vec, y_int, splits)

    # MLP
    def build_mlp_dataset(train_idx: np.ndarray, val_idx: np.ndarray):
        scaler = StandardScaler().fit(x_vec[train_idx])
        x_tr = scaler.transform(x_vec[train_idx])
        x_va = scaler.transform(x_vec[val_idx])
        return VectorDataset(x_tr, y_int[train_idx]), VectorDataset(x_va, y_int[val_idx])

    mlp_metrics, mlp_pred = run_nn_cv(
        make_model=lambda: MLP(n_classes=len(class_labels), input_dim=x_vec.shape[1]),
        train_dataset_builder=build_mlp_dataset,
        y_int=y_int,
        splits=splits,
        epochs=epochs_mlp,
        lr=lr_vec,
        batch_size=batch_size_vec,
        patience=early_stop_patience,
        weight_decay=weight_decay,
        device=device,
        num_workers=num_workers_dl,
    )

    # 1D-CNN
    cnn1d_channels, cnn1d_seq_len = infer_cnn1d_shape(x_vec.shape[1], clip_used)

    def build_cnn1d_dataset(train_idx: np.ndarray, val_idx: np.ndarray):
        scaler = StandardScaler().fit(x_vec[train_idx])
        x_tr = scaler.transform(x_vec[train_idx])
        x_va = scaler.transform(x_vec[val_idx])
        return VectorDataset(x_tr, y_int[train_idx]), VectorDataset(x_va, y_int[val_idx])

    cnn1d_metrics, cnn1d_pred = run_nn_cv(
        make_model=lambda: CNN1D(
            n_classes=len(class_labels),
            in_channels=cnn1d_channels,
            seq_len=cnn1d_seq_len,
        ),
        train_dataset_builder=build_cnn1d_dataset,
        y_int=y_int,
        splits=splits,
        epochs=epochs_cnn1d,
        lr=lr_vec,
        batch_size=batch_size_vec,
        patience=early_stop_patience,
        weight_decay=weight_decay,
        device=device,
        num_workers=num_workers_dl,
    )

    # 3D-CNN
    grid_dataset = GridTrialDataset(
        grid_paths=grid_paths,
        y=y_int,
        valid_frames=valid_frames_cache,
        clip_frames=clip_used,
        normalize_by_trial=normalize_grids,
    )

    def build_grid_dataset(train_idx: np.ndarray, val_idx: np.ndarray):
        return Subset(grid_dataset, train_idx), Subset(grid_dataset, val_idx)

    cnn3d_metrics, cnn3d_pred = run_nn_cv(
        make_model=lambda: CNN3D(
            n_classes=len(class_labels),
            in_channels=grid_dataset.in_channels,
        ),
        train_dataset_builder=build_grid_dataset,
        y_int=y_int,
        splits=splits,
        epochs=epochs_cnn3d,
        lr=lr_cnn3d,
        batch_size=batch_size_grid,
        patience=early_stop_patience,
        weight_decay=weight_decay,
        device=device,
        num_workers=num_workers_dl,
    )

    model_metrics = {
        "logreg": logreg_metrics,
        "mlp": mlp_metrics,
        "cnn1d": cnn1d_metrics,
        "cnn3d": cnn3d_metrics,
    }
    model_preds = {
        "logreg": logreg_pred,
        "mlp": mlp_pred,
        "cnn1d": cnn1d_pred,
        "cnn3d": cnn3d_pred,
    }
    best_model = max(model_order, key=lambda m: model_metrics[m]["mean_f1"])

    result = {
        "shuffle_id": shuffle_id,
        "mouse": mouse_name,
        "n_trials": int(len(y_int)),
        "cv_folds": folds,
        "n_features": int(x_vec.shape[1]),
        "clip_frames": clip_used,
        "cnn1d_channels": int(cnn1d_channels),
        "cnn1d_seq_len": int(cnn1d_seq_len),
        "class_labels": class_labels,
        "n_classes": len(class_labels),
        "best_model": best_model,
        "models": model_metrics,
    }

    model_cms = {
        mk: confusion_matrix(y_int, model_preds[mk], labels=np.arange(len(class_labels)))
        for mk in model_order
    }
    cm_payload = {
        "labels": class_labels,
        "cms": model_cms,
        "best_model": best_model,
    }

    return result, cm_payload


def _save_summary_figures_for_group(
    *,
    group_name: str,
    per_mouse_group: Dict[str, Dict[str, Any]],
    confusion_group: Dict[str, Dict[str, Any]],
    figures_dir: Path,
    method: str,
    grid_mode_label: str,
) -> List[Path]:
    """Save ablation-style summary figures for one result group.

    The group can be one shuffle (e.g. shuffle0003) or an aggregate
    (e.g. avg_across_shuffles).
    """
    model_order = ["logreg", "mlp", "cnn1d", "cnn3d"]
    model_titles = {
        "logreg": "LogReg (vector)",
        "mlp": "MLP (vector)",
        "cnn1d": "1D-CNN (vector)",
        "cnn3d": "3D-CNN (grid)",
    }
    colors = {
        "logreg": "#4C72B0",
        "mlp": "#55A868",
        "cnn1d": "#C44E52",
        "cnn3d": "#DD8452",
    }
    offsets = {
        "logreg": -1.5 * 0.18,
        "mlp": -0.5 * 0.18,
        "cnn1d": 0.5 * 0.18,
        "cnn3d": 1.5 * 0.18,
    }
    width = 0.18

    mice_order = sorted(per_mouse_group.keys())
    if not mice_order:
        return []

    written: List[Path] = []

    # Figure 1: per-mouse grouped bars (F1 + accuracy)
    x = np.arange(len(mice_order))
    fig, (ax_f1, ax_acc) = plt.subplots(
        2,
        1,
        figsize=(max(9, len(mice_order) * 1.3), 9.0),
        sharex=True,
    )
    for mk in model_order:
        vals_f1 = [float(per_mouse_group[m]["models"][mk]["mean_f1"]) for m in mice_order]
        errs_f1 = [float(per_mouse_group[m]["models"][mk].get("std_f1", 0.0)) for m in mice_order]
        vals_acc = [float(per_mouse_group[m]["models"][mk]["mean_acc"]) for m in mice_order]
        errs_acc = [float(per_mouse_group[m]["models"][mk].get("std_acc", 0.0)) for m in mice_order]

        ax_f1.bar(
            x + offsets[mk],
            vals_f1,
            width,
            yerr=errs_f1,
            capsize=3,
            label=model_titles[mk],
            alpha=0.85,
            color=colors[mk],
        )
        ax_acc.bar(
            x + offsets[mk],
            vals_acc,
            width,
            yerr=errs_acc,
            capsize=3,
            label=model_titles[mk],
            alpha=0.85,
            color=colors[mk],
        )

    ax_f1.set_ylim(0, 1.05)
    ax_f1.set_ylabel("Macro-F1")
    ax_f1.set_title(
        f"Within-mouse shuffle ablation by mouse ({method}, {grid_mode_label}) - {group_name}"
    )
    ax_f1.grid(axis="y", alpha=0.25)
    ax_f1.legend(loc="upper right", ncol=2, fontsize=8)

    ax_acc.set_xticks(x)
    ax_acc.set_xticklabels([_short_mouse_name(m) for m in mice_order], rotation=30, ha="right", fontsize=8)
    ax_acc.set_ylim(0, 1.05)
    ax_acc.set_ylabel("Accuracy")
    ax_acc.set_title(
        f"Within-mouse shuffle ablation accuracy by mouse ({method}, {grid_mode_label}) - {group_name}"
    )
    ax_acc.grid(axis="y", alpha=0.25)

    fig.tight_layout()
    fig1 = figures_dir / f"01_ablation_macro_f1_by_mouse_{group_name}.png"
    fig.savefig(fig1, dpi=300, bbox_inches="tight")
    plt.close(fig)
    written.append(fig1)

    # Figure 2: mean across mice
    fig, axes = plt.subplots(1, 2, figsize=(10, 4.6))
    mean_acc = [np.mean([per_mouse_group[m]["models"][mk]["mean_acc"] for m in mice_order]) for mk in model_order]
    mean_f1 = [np.mean([per_mouse_group[m]["models"][mk]["mean_f1"] for m in mice_order]) for mk in model_order]
    std_acc = [np.std([per_mouse_group[m]["models"][mk]["mean_acc"] for m in mice_order]) for mk in model_order]
    std_f1 = [np.std([per_mouse_group[m]["models"][mk]["mean_f1"] for m in mice_order]) for mk in model_order]

    axes[0].bar(
        np.arange(len(model_order)),
        mean_acc,
        yerr=std_acc,
        capsize=4,
        color=[colors[m] for m in model_order],
        alpha=0.85,
    )
    axes[0].set_xticks(np.arange(len(model_order)))
    axes[0].set_xticklabels([model_titles[m] for m in model_order], rotation=25, ha="right", fontsize=8)
    axes[0].set_ylim(0, 1.05)
    axes[0].set_ylabel("Accuracy")
    axes[0].set_title(f"Mean accuracy across mice ({group_name}, {grid_mode_label})")
    axes[0].grid(axis="y", alpha=0.25)

    axes[1].bar(
        np.arange(len(model_order)),
        mean_f1,
        yerr=std_f1,
        capsize=4,
        color=[colors[m] for m in model_order],
        alpha=0.85,
    )
    axes[1].set_xticks(np.arange(len(model_order)))
    axes[1].set_xticklabels([model_titles[m] for m in model_order], rotation=25, ha="right", fontsize=8)
    axes[1].set_ylim(0, 1.05)
    axes[1].set_ylabel("Macro-F1")
    axes[1].set_title(f"Mean macro-F1 across mice ({group_name}, {grid_mode_label})")
    axes[1].grid(axis="y", alpha=0.25)

    fig.tight_layout()
    fig2 = figures_dir / f"02_ablation_mean_scores_{group_name}.png"
    fig.savefig(fig2, dpi=300, bbox_inches="tight")
    plt.close(fig)
    written.append(fig2)

    # Figure 3: normalized confusion matrices for all mice (rows) x models (cols)
    n_mice = len(mice_order)
    n_models = len(model_order)
    fig, axes = plt.subplots(
        n_mice,
        n_models,
        figsize=(4.4 * n_models, 3.8 * n_mice),
        squeeze=False,
    )
    for row_idx, mouse_name in enumerate(mice_order):
        payload = confusion_group[mouse_name]
        labels_order = payload["labels"]
        best_model = payload["best_model"]
        for col_idx, mk in enumerate(model_order):
            ax = axes[row_idx][col_idx]
            cm = np.asarray(payload["cms"][mk], dtype=float)
            row_sums = cm.sum(axis=1, keepdims=True)
            cm_norm = np.divide(cm, row_sums, out=np.zeros_like(cm), where=row_sums != 0)
            ConfusionMatrixDisplay(cm_norm, display_labels=labels_order).plot(
                ax=ax,
                cmap="Blues",
                colorbar=False,
                values_format=".2f",
            )
            title = f"{_short_mouse_name(mouse_name)}\\n{model_titles[mk]}"
            if mk == best_model:
                title += " ★"
            ax.set_title(title, fontsize=7)

    fig.suptitle(
        f"Normalized confusion matrices ({group_name}, {grid_mode_label}) - all classifiers per mouse (★ = best)",
        fontsize=11,
    )
    fig.tight_layout()
    fig3 = figures_dir / f"03_all_classifier_confusion_matrices_{group_name}.png"
    fig.savefig(fig3, dpi=300, bbox_inches="tight")
    plt.close(fig)
    written.append(fig3)

    return written


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    parser = _build_parser()
    args = parser.parse_args()

    zz_folder = args.zz_folder or _build_zz_folder(args.p_active, args.per_trial_thresh)

    # Discover / validate mice
    discovered = _discover_mice(args.data_root)
    eligible_mice = validate_mice_for_ablation(
        args.mice, args.data_root, args.meta_root, discovered
    )
    if not eligible_mice:
        print("ERROR: No eligible mice found.", file=sys.stderr)
        sys.exit(1)

    # Determine clip frames
    if args.clip_frames is not None:
        global_clip = int(args.clip_frames)
    else:
        print("Pre-scanning valid_frames for global clip ...", flush=True)
        min_list = []
        for mn in eligible_mice:
            try:
                _, _, _, vf = load_labelled_barcodes(
                    args.data_root, args.meta_root, mn, zz_folder, max_trials=args.max_trials
                )
                if len(vf) > 0:
                    min_list.append(int(vf.min()))
            except Exception:
                pass
        if not min_list:
            print("ERROR: Could not determine global clip_frames.", file=sys.stderr)
            sys.exit(1)
        global_clip = min(min_list)
        print(f"  global_clip_frames = {global_clip}", flush=True)

    # ---- PREFLIGHT CHECKS --------------------------------------------------
    print("\nRunning preflight checks ...", flush=True)
    sampled_ids = _preflight_check(
        eligible_mice=eligible_mice,
        data_root=args.data_root,
        cache_dir=args.cache_dir,
        shuffle_type=args.shuffle_type,
        method=args.vectorization_method,
        p_active=args.p_active,
        per_trial_thresh=args.per_trial_thresh,
        clip_frames=global_clip,
        different_shuffle_per_trial=args.different_shuffle_per_trial,
        n_shuffles=args.n_shuffles,
        max_trials=args.max_trials,
        seed=args.seed,
    )
    print(f"Preflight PASSED. Sampled shuffle IDs per mouse:", flush=True)
    for mn, ids in sampled_ids.items():
        print(f"  {mn}: {ids}", flush=True)
    # -------------------------------------------------------------------------

    mode_token = _shuffle_mode_token(args.different_shuffle_per_trial)
    output_dir = args.output_folder / mode_token
    output_dir.mkdir(parents=True, exist_ok=True)
    figures_dir = output_dir / "figures"
    logs_dir = output_dir / "logs"
    figures_dir.mkdir(parents=True, exist_ok=True)
    logs_dir.mkdir(parents=True, exist_ok=True)
    log_path = logs_dir / "run.log"

    device = _resolve_device(args.device)
    _set_seed(args.seed)

    fs = _FakeState(cache_dir=args.cache_dir, data_root=args.data_root)

    with open(log_path, "w", encoding="utf-8") as log_fp, \
         redirect_stdout(log_fp), redirect_stderr(log_fp):

        print("=" * 90)
        print("Within-Mouse Classification on Shuffled Vectorizations")
        print(f"Timestamp: {datetime.now().isoformat(timespec='seconds')}")
        print("=" * 90)
        print(f"  output_folder        : {output_dir}")
        print(f"  shuffle_type         : {args.shuffle_type}")
        print(f"  n_shuffles (sampled) : {args.n_shuffles}")
        print(f"  shuffle_mode         : {mode_token}")
        print(f"  method               : {args.vectorization_method}")
        print(f"  p_active             : {args.p_active}")
        print(f"  clip_frames          : {global_clip}")
        print(f"  normalize_grids      : {args.normalize_grids}")
        print(f"  device               : {device}")
        print(f"  seed                 : {args.seed}")

        grid_mode_label = "trial-l1-normalized-grid" if args.normalize_grids else "raw-grid"
        print("\nSampled shuffle IDs:")
        for mn, ids in sampled_ids.items():
            print(f"  {mn}: {ids}")

        # Containers for results
        all_results: Dict[str, Dict[str, Any]] = {}  # {mouse: {shuffle_id: result}}
        all_cms: Dict[str, Dict[str, Any]] = {}

        for mouse_name in eligible_mice:
            print(f"\n{'='*70}")
            print(f"Mouse: {mouse_name}")
            all_results[mouse_name] = {}
            all_cms[mouse_name] = {}

            base_stem = build_vectorization_cache_stem(
                mouse_name=mouse_name,
                method=args.vectorization_method,
                p_active=args.p_active,
                per_trial_thresh=args.per_trial_thresh,
                clip_frames=global_clip,
            )
            mcdir = _resolve_mouse_cache_dir(fs, mouse_name)

            for shuffle_id in sampled_ids[mouse_name]:
                shuffle_stem = _shuffle_cache_stem_with_mode(
                    base_stem,
                    args.shuffle_type,
                    shuffle_id,
                    args.different_shuffle_per_trial,
                )
                cache_path = mcdir / f"{shuffle_stem}.npz"

                print(f"\n  [shuffle {shuffle_id}]", end=" ", flush=True)
                try:
                    result, cm_payload = _run_within_mouse_one_shuffle(
                        mouse_name=mouse_name,
                        shuffle_id=shuffle_id,
                        cache_path=cache_path,
                        clip_frames=global_clip,
                        n_splits=args.n_splits,
                        device=device,
                        epochs_mlp=args.epochs_mlp,
                        epochs_cnn1d=args.epochs_cnn1d,
                        epochs_cnn3d=args.epochs_cnn3d,
                        lr_vec=args.lr_vec,
                        lr_cnn3d=args.lr_cnn3d,
                        batch_size_vec=args.batch_size_vec,
                        batch_size_grid=args.batch_size_grid,
                        normalize_grids=args.normalize_grids,
                        weight_decay=args.weight_decay,
                        early_stop_patience=args.early_stop_patience,
                        num_workers_dl=args.num_workers_dl,
                        seed=args.seed,
                    )
                    if result is not None:
                        all_results[mouse_name][shuffle_id] = result
                        all_cms[mouse_name][shuffle_id] = cm_payload
                        best = result["best_model"]
                        f1 = result["models"][best].get("mean_f1", 0)
                        print(f"ok (best={best}:{f1:.3f})")
                except Exception as exc:
                    print(f"FAILED: {exc}")
                    traceback.print_exc()

                gc.collect()

        # ---- Save figures ---------------------------------------------------
        print("\nSaving ablation-style figures (per shuffle + average across shuffles) ...", flush=True)
        written_figures: List[Path] = []

        # Build per-shuffle grouped views: {shuffle_id -> per_mouse_result/payload}
        shuffle_ids_present = sorted({sid for per_mouse in all_results.values() for sid in per_mouse.keys()})
        for shuffle_id in shuffle_ids_present:
            per_mouse_group: Dict[str, Dict[str, Any]] = {}
            confusion_group: Dict[str, Dict[str, Any]] = {}
            for mouse_name in eligible_mice:
                if shuffle_id in all_results.get(mouse_name, {}):
                    per_mouse_group[mouse_name] = all_results[mouse_name][shuffle_id]
                if shuffle_id in all_cms.get(mouse_name, {}):
                    confusion_group[mouse_name] = all_cms[mouse_name][shuffle_id]

            if per_mouse_group and confusion_group:
                gname = f"shuffle{int(shuffle_id):04d}"
                written_figures.extend(
                    _save_summary_figures_for_group(
                        group_name=gname,
                        per_mouse_group=per_mouse_group,
                        confusion_group=confusion_group,
                        figures_dir=figures_dir,
                        method=args.vectorization_method,
                        grid_mode_label=grid_mode_label,
                    )
                )

        # Average across shuffles: per-mouse model metrics averaged over available shuffles,
        # and confusion matrices averaged element-wise over shuffles.
        avg_per_mouse: Dict[str, Dict[str, Any]] = {}
        avg_cms: Dict[str, Dict[str, Any]] = {}
        for mouse_name in eligible_mice:
            shuf_results = all_results.get(mouse_name, {})
            shuf_cms = all_cms.get(mouse_name, {})
            if not shuf_results or not shuf_cms:
                continue

            shuffle_keys = sorted(set(shuf_results.keys()) & set(shuf_cms.keys()))
            if not shuffle_keys:
                continue

            _all_model_keys = ["logreg", "mlp", "cnn1d", "cnn3d"]
            avg_models = {
                mk: {
                    "mean_acc": float(np.mean([float(shuf_results[s]["models"][mk]["mean_acc"]) for s in shuffle_keys])),
                    "std_acc": float(np.std([float(shuf_results[s]["models"][mk]["mean_acc"]) for s in shuffle_keys])),
                    "mean_f1": float(np.mean([float(shuf_results[s]["models"][mk]["mean_f1"]) for s in shuffle_keys])),
                    "std_f1": float(np.std([float(shuf_results[s]["models"][mk]["mean_f1"]) for s in shuffle_keys])),
                }
                for mk in _all_model_keys
            }
            best_model = max(_all_model_keys, key=lambda m: avg_models[m]["mean_f1"])

            first_row = shuf_results[shuffle_keys[0]]
            avg_per_mouse[mouse_name] = {
                "shuffle_id": "avg",
                "mouse": mouse_name,
                "n_trials": int(first_row["n_trials"]),
                "cv_folds": int(first_row["cv_folds"]),
                "n_features": int(first_row["n_features"]),
                "clip_frames": int(first_row["clip_frames"]),
                "class_labels": list(first_row["class_labels"]),
                "n_classes": int(first_row["n_classes"]),
                "best_model": best_model,
                "models": avg_models,
            }

            labels_order = list(shuf_cms[shuffle_keys[0]]["labels"])
            avg_cms[mouse_name] = {
                "labels": labels_order,
                "cms": {
                    mk: np.mean(
                        np.stack([np.asarray(shuf_cms[s]["cms"][mk], dtype=float) for s in shuffle_keys], axis=0),
                        axis=0,
                    )
                    for mk in _all_model_keys
                },
                "best_model": best_model,
            }

        if avg_per_mouse and avg_cms:
            written_figures.extend(
                _save_summary_figures_for_group(
                    group_name="avg_across_shuffles",
                    per_mouse_group=avg_per_mouse,
                    confusion_group=avg_cms,
                    figures_dir=figures_dir,
                    method=args.vectorization_method,
                    grid_mode_label=grid_mode_label,
                )
            )

        # ---- Save JSON / CSV ------------------------------------------------
        json_path = output_dir / "within_mouse_ablation_shuffle_metrics.json"
        csv_path = output_dir / "within_mouse_ablation_shuffle_metrics.csv"

        payload_json = {
            "method": args.vectorization_method,
            "p_active": args.p_active,
            "shuffle_type": args.shuffle_type,
            "different_shuffle_per_trial": args.different_shuffle_per_trial,
            "shuffle_mode": mode_token,
            "normalize_grids": args.normalize_grids,
            "n_shuffles_requested": args.n_shuffles,
            "seed": args.seed,
            "eligible_mice": eligible_mice,
            "sampled_shuffle_ids": sampled_ids,
            "per_mouse": all_results,
            "figures": [str(p) for p in written_figures],
        }
        with open(json_path, "w", encoding="utf-8") as fp:
            json.dump(payload_json, fp, indent=2)
        print(f"Wrote JSON: {json_path}")

        with open(csv_path, "w", encoding="utf-8", newline="") as fp:
            writer = csv.writer(fp)
            writer.writerow([
                "shuffle_mode",
                "different_shuffle_per_trial",
                "normalize_grids",
                "mouse", "shuffle_id", "model",
                "n_trials", "cv_folds",
                "mean_acc", "std_acc", "mean_f1", "std_f1",
                "best_model",
            ])
            for mouse_name, shuf_results in all_results.items():
                for shuffle_id, row in shuf_results.items():
                    for mk in ["logreg", "mlp", "cnn1d", "cnn3d"]:
                        mr = row["models"][mk]
                        writer.writerow([
                            mode_token,
                            args.different_shuffle_per_trial,
                            args.normalize_grids,
                            mouse_name,
                            shuffle_id,
                            mk,
                            row["n_trials"],
                            row["cv_folds"],
                            mr["mean_acc"],
                            mr["std_acc"],
                            mr["mean_f1"],
                            mr["std_f1"],
                            row["best_model"],
                        ])
        print(f"Wrote CSV : {csv_path}")

        print("\n" + "=" * 90)
        print("SUCCESS")
        print("=" * 90)

    print(f"Done. Results: {output_dir}", flush=True)


if __name__ == "__main__":
    main()
